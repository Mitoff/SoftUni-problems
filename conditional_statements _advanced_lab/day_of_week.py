number = int(input())

if number == 1:
    print(f'Monday')
elif number == 2:
    print(f'Tuesday')
elif number == 3:
    print(f'Wednesday')
elif number == 4:
    print(f'Thursday')
elif number == 5:
    print(f'Friday')
elif number == 6:
    print(f'Saturday')
elif number == 7:
    print(f'Sunday')
else:
    print('Error')
